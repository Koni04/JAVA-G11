// Program to print a text 5 times

public class Forloop 
{
    public static void main(String[] args) // main class
    {
  
      int n = 10;  // a value of int n is 5
      // for loop  
      for (int i = 1; i <= n; ++i)  // int index = 1; i greater than or equal to 5; is addition i++ increment
      {
        System.out.println("HAHAHAHHAHAAHAHAHAHHAHAAHAHAHAHHAHAAHAHAHAHHAHAAHAHAHAHHAHAA"); // it will print a value with 5 times!
      }
    }
}